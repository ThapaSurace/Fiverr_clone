import mongoose from "mongoose";
const { Schema } = mongoose;


const gigSchema = new Schema(
  {
    userId: {
      type:String,
      required: true,
    },
    title: {
      type: String,
      required: true,
    },
    desc: {
      type: String,
      required: true,
    },
    totalStars: {
      type: Number,
      default: 0,
    },
    starNumber: {
      type: Number,
      default: 0,
    },
    cat: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
    cover: {
      type: String,
      required: true,
    },
    images: {
      type: [String],
      required: false,
    },
    shortTitle: {
      type: String,
      required: true,
    },
    shortDesc: {
      type: String,
      required: true,
    },
    deliveryTime: {
      type: Number,
      required: true,
    },
    revisionNumber: {
      type: Number,
      required: true,
    },
    features: {
      type: [String],
      required: false,
    },
    sales: {
      type: Number,
      default: 0,
    },
  },
  {
    timestamps: true,
  }
);

// Virtual property to calculate the average rating
gigSchema.virtual('averageRating').get(function () {
  if (this.starNumber === 0) {
    return 0;
  }
  return this.totalStars / this.starNumber;
});

export default mongoose.model.Gigs || mongoose.model("Gig", gigSchema);
